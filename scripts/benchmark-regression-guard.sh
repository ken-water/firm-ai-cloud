#!/usr/bin/env bash
set -Eeuo pipefail

RUN_ID="${RUN_ID:-$(date -u +%Y%m%dT%H%M%SZ)}"
TREND_SUMMARY=""
PROFILE_LABEL="${PROFILE_LABEL:-}"
POLICY_FILE="${POLICY_FILE:-scripts/benchmark-regression-policy.json}"
OUTPUT_DIR="${OUTPUT_DIR:-.run/benchmarks/regression-guard-${RUN_ID}}"
SUMMARY_JSON="${OUTPUT_DIR}/summary.json"
SUMMARY_MD="${OUTPUT_DIR}/summary.md"

TMP_CHECKS_FILE=""
RESOLVED_POLICY_FILE=""
POLICY_SOURCE_PATH="root"

log() {
  printf '[benchmark-guard] %s\n' "$*"
}

fatal() {
  printf '[benchmark-guard][ERROR] %s\n' "$*" >&2
  exit 1
}

usage() {
  cat <<'USAGE'
Benchmark regression guard

Usage:
  bash scripts/benchmark-regression-guard.sh [options]

Options:
  --trend-summary <path>   Trend delta summary json generated by benchmark-trend-delta.sh
  --profile <label>        Profile label (defaults to .profile from trend summary)
  --policy <path>          Regression policy json (default: scripts/benchmark-regression-policy.json)
  --output-dir <path>      Output directory for guard summary
  -h, --help               Show this help
USAGE
}

require_cmd() {
  command -v "$1" >/dev/null 2>&1 || fatal "missing required command: $1"
}

is_number() {
  [[ "$1" =~ ^-?[0-9]+([.][0-9]+)?$ ]]
}

num_ge() {
  local actual="$1"
  local threshold="$2"
  awk -v a="${actual}" -v b="${threshold}" 'BEGIN { if ((a + 0) >= (b + 0)) print 1; else print 0 }'
}

num_le() {
  local actual="$1"
  local threshold="$2"
  awk -v a="${actual}" -v b="${threshold}" 'BEGIN { if ((a + 0) <= (b + 0)) print 1; else print 0 }'
}

append_check() {
  local category="$1"
  local target="$2"
  local metric="$3"
  local delta="$4"
  local threshold="$5"
  local operator="$6"
  local pass="$7"
  local note="$8"

  local pass_json="false"
  if [[ "${pass}" -eq 1 ]]; then
    pass_json="true"
  fi

  jq -nc \
    --arg category "${category}" \
    --arg target "${target}" \
    --arg metric "${metric}" \
    --arg delta "${delta}" \
    --arg threshold "${threshold}" \
    --arg operator "${operator}" \
    --arg note "${note}" \
    --argjson pass "${pass_json}" \
    '{
      category: $category,
      target: $target,
      metric: $metric,
      delta: $delta,
      threshold: $threshold,
      operator: $operator,
      pass: $pass,
      note: $note
    }' >>"${TMP_CHECKS_FILE}"
}

resolve_profile() {
  if [[ -z "${PROFILE_LABEL}" ]]; then
    PROFILE_LABEL="$(jq -r '.profile // empty' "${TREND_SUMMARY}")"
  fi
  [[ -n "${PROFILE_LABEL}" ]] || fatal "unable to determine profile label (use --profile)"
}

resolve_profile_policy() {
  RESOLVED_POLICY_FILE="$(mktemp)"

  if jq -e '.profiles? | type == "object"' "${POLICY_FILE}" >/dev/null; then
    local available_profiles
    available_profiles="$(jq -r '.profiles | keys | join(", ")' "${POLICY_FILE}")"

    if ! jq -e --arg profile "${PROFILE_LABEL}" '.profiles[$profile] != null' "${POLICY_FILE}" >/dev/null; then
      fatal "profile '${PROFILE_LABEL}' not found in policy '${POLICY_FILE}'. available profiles: ${available_profiles}"
    fi

    jq --arg profile "${PROFILE_LABEL}" '.profiles[$profile]' "${POLICY_FILE}" >"${RESOLVED_POLICY_FILE}"
    POLICY_SOURCE_PATH="profiles.${PROFILE_LABEL}"
  else
    cp "${POLICY_FILE}" "${RESOLVED_POLICY_FILE}"
    POLICY_SOURCE_PATH="root"
  fi
}

evaluate_metric() {
  local category="$1"
  local metric="$2"
  local operator="$3"
  local threshold="$4"
  local unit_note="$5"

  mapfile -t rows < <(jq -c --arg category "${category}" --arg metric "${metric}" '.deltas[] | select(.category == $category and .metric == $metric)' "${TREND_SUMMARY}")
  if [[ ${#rows[@]} -eq 0 ]]; then
    append_check "${category}" "*" "${metric}" "missing" "${threshold}" "${operator}" 0 "delta missing in trend summary (${unit_note})"
    return
  fi

  local row
  for row in "${rows[@]}"; do
    local target delta pass note
    target="$(echo "${row}" | jq -r '.target')"
    delta="$(echo "${row}" | jq -r '.delta')"
    pass=0
    note="${unit_note}"

    if ! is_number "${delta}"; then
      note="non-numeric delta (${unit_note})"
    elif [[ "${operator}" == ">=" ]]; then
      pass="$(num_ge "${delta}" "${threshold}")"
    elif [[ "${operator}" == "<=" ]]; then
      pass="$(num_le "${delta}" "${threshold}")"
    else
      note="unsupported operator '${operator}'"
    fi

    append_check "${category}" "${target}" "${metric}" "${delta}" "${threshold}" "${operator}" "${pass}" "${note}"
  done
}

run_checks() {
  local api_success_min api_p95_max api_p99_max
  local sse_lag_p95_max sse_lag_p99_max sse_lag_max_max sse_alert_sync_min sse_stream_error_max

  api_success_min="$(jq -r '.api.success_rate_min_delta' "${RESOLVED_POLICY_FILE}")"
  api_p95_max="$(jq -r '.api.p95_ms_max_delta' "${RESOLVED_POLICY_FILE}")"
  api_p99_max="$(jq -r '.api.p99_ms_max_delta' "${RESOLVED_POLICY_FILE}")"

  sse_lag_p95_max="$(jq -r '.sse.lag_p95_ms_max_delta' "${RESOLVED_POLICY_FILE}")"
  sse_lag_p99_max="$(jq -r '.sse.lag_p99_ms_max_delta' "${RESOLVED_POLICY_FILE}")"
  sse_lag_max_max="$(jq -r '.sse.lag_max_ms_max_delta' "${RESOLVED_POLICY_FILE}")"
  sse_alert_sync_min="$(jq -r '.sse.alert_monitoring_sync_count_min_delta' "${RESOLVED_POLICY_FILE}")"
  sse_stream_error_max="$(jq -r '.sse.stream_error_count_max_delta' "${RESOLVED_POLICY_FILE}")"

  evaluate_metric "api" "success_rate" ">=" "${api_success_min}" "percentage delta"
  evaluate_metric "api" "p95_ms" "<=" "${api_p95_max}" "milliseconds delta"
  evaluate_metric "api" "p99_ms" "<=" "${api_p99_max}" "milliseconds delta"

  evaluate_metric "sse" "lag_p95_ms" "<=" "${sse_lag_p95_max}" "milliseconds delta"
  evaluate_metric "sse" "lag_p99_ms" "<=" "${sse_lag_p99_max}" "milliseconds delta"
  evaluate_metric "sse" "lag_max_ms" "<=" "${sse_lag_max_max}" "milliseconds delta"
  evaluate_metric "sse" "alert_monitoring_sync_count" ">=" "${sse_alert_sync_min}" "count delta"
  evaluate_metric "sse" "stream_error_count" "<=" "${sse_stream_error_max}" "count delta"
}

write_summaries() {
  local generated_at overall_pass_json failed_count checks_count
  generated_at="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  checks_count="$(jq -s 'length' "${TMP_CHECKS_FILE}")"
  failed_count="$(jq -s 'map(select(.pass == false)) | length' "${TMP_CHECKS_FILE}")"

  if [[ "${failed_count}" == "0" ]]; then
    overall_pass_json="true"
  else
    overall_pass_json="false"
  fi

  jq -n \
    --arg run_id "${RUN_ID}" \
    --arg generated_at "${generated_at}" \
    --arg profile "${PROFILE_LABEL}" \
    --arg trend_summary "${TREND_SUMMARY}" \
    --arg policy_file "${POLICY_FILE}" \
    --arg policy_source "${POLICY_SOURCE_PATH}" \
    --argjson pass "${overall_pass_json}" \
    --slurpfile checks "${TMP_CHECKS_FILE}" \
    '{
      run_id: $run_id,
      generated_at: $generated_at,
      profile: $profile,
      trend_summary: $trend_summary,
      policy_file: $policy_file,
      policy_source: $policy_source,
      checks: $checks,
      pass: $pass,
      totals: {
        checks: ($checks | length),
        failed: ($checks | map(select(.pass == false)) | length)
      }
    }' >"${SUMMARY_JSON}"

  local result_text="PASS"
  if [[ "${overall_pass_json}" != "true" ]]; then
    result_text="FAIL"
  fi

  {
    echo "# Benchmark Regression Guard Summary"
    echo
    echo "- Run ID: ${RUN_ID}"
    echo "- Generated at: ${generated_at}"
    echo "- Profile: ${PROFILE_LABEL}"
    echo "- Trend summary: \`${TREND_SUMMARY}\`"
    echo "- Policy: \`${POLICY_FILE}\`"
    echo "- Policy source: \`${POLICY_SOURCE_PATH}\`"
    echo "- Result: **${result_text}**"
    echo
    echo "| Category | Target | Metric | Delta | Threshold | Operator | Result | Note |"
    echo "| --- | --- | --- | --- | --- | --- | --- | --- |"
    jq -r '.checks[] | "| `\(.category)` | `\(.target)` | `\(.metric)` | `\(.delta)` | `\(.threshold)` | `\(.operator)` | \((if .pass then "PASS" else "FAIL" end)) | \(.note) |"' "${SUMMARY_JSON}"

    if [[ "${failed_count}" != "0" ]]; then
      echo
      echo "## Impacted Metrics"
      jq -r '.checks[] | select(.pass == false) | "- \(.category)/\(.target)/\(.metric): delta=\(.delta), threshold \(.operator) \(.threshold)"' "${SUMMARY_JSON}"
    fi
  } >"${SUMMARY_MD}"

  log "Regression guard summary JSON: ${SUMMARY_JSON}"
  log "Regression guard summary Markdown: ${SUMMARY_MD}"

  if [[ "${overall_pass_json}" != "true" ]]; then
    fatal "benchmark regression guard failed"
  fi
}

main() {
  require_cmd jq
  require_cmd awk

  while [[ $# -gt 0 ]]; do
    case "$1" in
      --trend-summary)
        [[ $# -ge 2 ]] || fatal "--trend-summary requires a file path"
        TREND_SUMMARY="$2"
        shift 2
        ;;
      --profile)
        [[ $# -ge 2 ]] || fatal "--profile requires a label"
        PROFILE_LABEL="$2"
        shift 2
        ;;
      --policy)
        [[ $# -ge 2 ]] || fatal "--policy requires a file path"
        POLICY_FILE="$2"
        shift 2
        ;;
      --output-dir)
        [[ $# -ge 2 ]] || fatal "--output-dir requires a path"
        OUTPUT_DIR="$2"
        SUMMARY_JSON="${OUTPUT_DIR}/summary.json"
        SUMMARY_MD="${OUTPUT_DIR}/summary.md"
        shift 2
        ;;
      -h|--help)
        usage
        exit 0
        ;;
      *)
        fatal "unknown argument: $1"
        ;;
    esac
  done

  [[ -n "${TREND_SUMMARY}" ]] || fatal "--trend-summary is required"
  [[ -f "${TREND_SUMMARY}" ]] || fatal "trend summary file not found: ${TREND_SUMMARY}"
  [[ -f "${POLICY_FILE}" ]] || fatal "policy file not found: ${POLICY_FILE}"

  mkdir -p "${OUTPUT_DIR}"
  TMP_CHECKS_FILE="$(mktemp)"
  trap 'rm -f "${TMP_CHECKS_FILE}" "${RESOLVED_POLICY_FILE}"' EXIT
  : >"${TMP_CHECKS_FILE}"

  resolve_profile
  resolve_profile_policy
  run_checks

  local checks_count
  checks_count="$(jq -s 'length' "${TMP_CHECKS_FILE}")"
  [[ "${checks_count}" != "0" ]] || fatal "no regression checks were evaluated"

  write_summaries
}

main "$@"

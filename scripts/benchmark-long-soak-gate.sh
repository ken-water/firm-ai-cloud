#!/usr/bin/env bash
set -Eeuo pipefail

RUN_ID="${RUN_ID:-$(date -u +%Y%m%dT%H%M%SZ)}"
DRIFT_SUMMARY=""
POLICY_FILE="${POLICY_FILE:-scripts/benchmark-long-soak-policy.json}"
OUTPUT_DIR="${OUTPUT_DIR:-.run/benchmarks/long-soak-gate-${RUN_ID}}"
SUMMARY_JSON="${OUTPUT_DIR}/summary.json"
SUMMARY_MD="${OUTPUT_DIR}/summary.md"
TMP_CHECKS=""

log() {
  printf '[long-soak-gate] %s\n' "$*"
}

fatal() {
  printf '[long-soak-gate][ERROR] %s\n' "$*" >&2
  exit 1
}

usage() {
  cat <<'USAGE'
Long-soak stability gate

Usage:
  bash scripts/benchmark-long-soak-gate.sh [options]

Options:
  --drift-summary <path>   Drift summary json generated by benchmark-long-soak.sh
  --policy <path>          Policy file (default: scripts/benchmark-long-soak-policy.json)
  --output-dir <path>      Output directory
  -h, --help               Show help
USAGE
}

require_cmd() {
  command -v "$1" >/dev/null 2>&1 || fatal "missing required command: $1"
}

num_ge() {
  local actual="$1"
  local expected="$2"
  awk -v a="${actual}" -v b="${expected}" 'BEGIN { if ((a + 0) >= (b + 0)) print 1; else print 0 }'
}

num_le() {
  local actual="$1"
  local expected="$2"
  awk -v a="${actual}" -v b="${expected}" 'BEGIN { if ((a + 0) <= (b + 0)) print 1; else print 0 }'
}

metric_delta() {
  local metric="$1"
  jq -r --arg metric "${metric}" '.deltas[] | select(.metric == $metric) | .delta' "${DRIFT_SUMMARY}" | head -n1
}

append_check() {
  local metric="$1"
  local delta="$2"
  local threshold="$3"
  local operator="$4"
  local pass="$5"

  jq -nc \
    --arg metric "${metric}" \
    --arg delta "${delta}" \
    --arg threshold "${threshold}" \
    --arg operator "${operator}" \
    --argjson pass "${pass}" \
    '{metric:$metric, delta:$delta, threshold:$threshold, operator:$operator, pass:$pass}' >>"${TMP_CHECKS}"
}

evaluate_one() {
  local metric="$1"
  local operator="$2"
  local threshold="$3"
  local delta pass
  delta="$(metric_delta "${metric}")"
  [[ -n "${delta}" && "${delta}" != "null" ]] || fatal "metric '${metric}' missing in drift summary"

  if [[ "${operator}" == ">=" ]]; then
    pass="$(num_ge "${delta}" "${threshold}")"
  else
    pass="$(num_le "${delta}" "${threshold}")"
  fi
  append_check "${metric}" "${delta}" "${threshold}" "${operator}" "${pass}"
}

main() {
  require_cmd jq
  require_cmd awk

  while [[ $# -gt 0 ]]; do
    case "$1" in
      --drift-summary)
        [[ $# -ge 2 ]] || fatal "--drift-summary requires a file"
        DRIFT_SUMMARY="$2"
        shift 2
        ;;
      --policy)
        [[ $# -ge 2 ]] || fatal "--policy requires a file"
        POLICY_FILE="$2"
        shift 2
        ;;
      --output-dir)
        [[ $# -ge 2 ]] || fatal "--output-dir requires a path"
        OUTPUT_DIR="$2"
        SUMMARY_JSON="${OUTPUT_DIR}/summary.json"
        SUMMARY_MD="${OUTPUT_DIR}/summary.md"
        shift 2
        ;;
      -h|--help)
        usage
        exit 0
        ;;
      *)
        fatal "unknown argument: $1"
        ;;
    esac
  done

  [[ -n "${DRIFT_SUMMARY}" ]] || fatal "--drift-summary is required"
  [[ -f "${DRIFT_SUMMARY}" ]] || fatal "drift summary not found: ${DRIFT_SUMMARY}"
  [[ -f "${POLICY_FILE}" ]] || fatal "policy file not found: ${POLICY_FILE}"

  mkdir -p "${OUTPUT_DIR}"
  TMP_CHECKS="$(mktemp)"
  trap 'rm -f "${TMP_CHECKS}"' EXIT
  : >"${TMP_CHECKS}"

  evaluate_one "api_success_rate_avg" ">=" "$(jq -r '.api.success_rate_avg_min_delta' "${POLICY_FILE}")"
  evaluate_one "api_p95_ms_max" "<=" "$(jq -r '.api.p95_ms_max_delta' "${POLICY_FILE}")"
  evaluate_one "api_p99_ms_max" "<=" "$(jq -r '.api.p99_ms_max_delta' "${POLICY_FILE}")"
  evaluate_one "sse_lag_p95_ms" "<=" "$(jq -r '.sse.lag_p95_ms_max_delta' "${POLICY_FILE}")"
  evaluate_one "sse_lag_p99_ms" "<=" "$(jq -r '.sse.lag_p99_ms_max_delta' "${POLICY_FILE}")"
  evaluate_one "sse_lag_max_ms" "<=" "$(jq -r '.sse.lag_max_ms_max_delta' "${POLICY_FILE}")"
  evaluate_one "sse_stream_error_count" "<=" "$(jq -r '.sse.stream_error_count_max_delta' "${POLICY_FILE}")"

  local failed
  failed="$(jq -s 'map(select(.pass == false)) | length' "${TMP_CHECKS}")"
  local pass_json
  if [[ "${failed}" == "0" ]]; then
    pass_json=true
  else
    pass_json=false
  fi

  jq -n \
    --arg run_id "${RUN_ID}" \
    --arg generated_at "$(date -u +%Y-%m-%dT%H:%M:%SZ)" \
    --arg drift_summary "${DRIFT_SUMMARY}" \
    --arg policy_file "${POLICY_FILE}" \
    --argjson pass "${pass_json}" \
    --slurpfile checks "${TMP_CHECKS}" \
    '{
      run_id: $run_id,
      generated_at: $generated_at,
      drift_summary: $drift_summary,
      policy_file: $policy_file,
      pass: $pass,
      checks: $checks,
      totals: {
        checks: ($checks | length),
        failed: ($checks | map(select(.pass == false)) | length)
      }
    }' >"${SUMMARY_JSON}"

  {
    echo "# Long-Soak Gate Summary"
    echo
    echo "- Drift summary: \`${DRIFT_SUMMARY}\`"
    echo "- Policy file: \`${POLICY_FILE}\`"
    echo "- Result: **$(jq -r 'if .pass then "PASS" else "FAIL" end' "${SUMMARY_JSON}")**"
    echo
    echo "| Metric | Delta | Threshold | Operator | Result |"
    echo "| --- | --- | --- | --- | --- |"
    jq -r '.checks[] | "| `\(.metric)` | `\(.delta)` | `\(.threshold)` | `\(.operator)` | \((if .pass then "PASS" else "FAIL" end)) |"' "${SUMMARY_JSON}"
  } >"${SUMMARY_MD}"

  log "Gate summary JSON: ${SUMMARY_JSON}"
  log "Gate summary Markdown: ${SUMMARY_MD}"

  if [[ "${pass_json}" != "true" ]]; then
    fatal "long-soak gate failed"
  fi
}

main "$@"
